# Grading system in BD education govt

number = float(input("Enter The Number: "))
if number >= 80:
    print("You Have Gotten A+")
elif number >= 70:
    print("You Have Gotten A")
elif number >= 60:
    print("You Have Gotten A-")
elif number >= 50:
    print("You Have Gotten B")
elif number >= 40:
    print("You Have Gotten C")
elif number >= 33:
    print("You Have Gotten D")
else:
    print("You are fail")